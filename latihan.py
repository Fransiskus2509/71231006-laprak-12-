# List menjadi Set
list_data = [1, 2, 3, 4, 5]
set_data = set(list_data)
print("List menjadi Set:", set_data)

# Set menjadi List
set_data = {1, 2, 3, 4, 5}
list_data = list(set_data)
print("Set menjadi List:", list_data)

# Tuple menjadi Set
tuple_data = (1, 2, 3, 4, 5)
set_data = set(tuple_data)
print("Tuple menjadi Set:", set_data)

# Set menjadi Tuple
set_data = {1, 2, 3, 4, 5}
tuple_data = tuple(set_data)
print("Set menjadi Tuple:", tuple_data)